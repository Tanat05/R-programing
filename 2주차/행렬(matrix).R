mat1 <- matrix(c(1, 2, 3, 4))
mat1
#     [,1]
#[1,]    1
#[2,]    2
#[3,]    3
#[4,]    4


mat2 <- matrix(c(1, 2, 3, 4), nrow = 2)
mat2
#     [,1] [,2]
#[1,]    1    3
#[2,]    2    4


mat3 <- matrix(c(1, 2, 3, 4), nrow = 2, byrow = T)
mat3
#     [,1] [,2]
#[1,]    1    2
#[2,]    3    4

mat3
#     [,1] [,2]
#[1,]    1    2
#[2,]    3    4
mat3[,1]
#[1] 1 3
mat3[1,]
#[1] 1 2
mat3[1,1]
#[1] 1

mat4 <- matrix(c(1, 2, 3, 4, 5, 6, 7, 8, 9), nrow = 3, byrow = T)
mat4
#     [,1] [,2] [,3]
#[1,]    1    2    3
#[2,]    4    5    6
#[3,]    7    8    9

mat4 <- rbind(mat4, c(11, 12, 13))
mat4
#     [,1] [,2] [,3]
#[1,]    1    2    3
#[2,]    4    5    6
#[3,]    7    8    9
#[4,]   11   12   13

mat5 <- matrix(c('a', 'b', 'c', 'd'), nrow = 2, byrow = T)
